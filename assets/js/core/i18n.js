  let _lang = localStorage.getItem('lang') || 'en';

  function t(key) {
    return (translations[_lang] && translations[_lang][key]) || key;
  }

  function setLang(lang) {
    _lang = lang;
    localStorage.setItem('lang', lang);
    _applyAll();
  }

  function _applyAll() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      el.textContent = t(key);
    });
    document.querySelectorAll('[data-lang-select]').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.langSelect === _lang);
    });
    const html = document.querySelector('html');
    if (html) html.lang = _lang;
  }

  function init() {
    document.querySelectorAll('[data-lang-select]').forEach(btn => {
      btn.addEventListener('click', () => setLang(btn.dataset.langSelect));
    });
    _applyAll();
  }

  return { init, t, setLang };
